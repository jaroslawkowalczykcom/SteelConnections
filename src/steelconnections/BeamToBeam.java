    public double roundUpToNplace(double number, int numberAfterComa){
           String s = String.valueOf(number);
           double zaokr;
           zaokr = Double.parseDouble(s);
           zaokr *= numberAfterComa;
           zaokr = Math.round(zaokr);
           zaokr /= numberAfterComa;
           
           return zaokr;
}
    
